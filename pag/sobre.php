                    <p class="paragraph"> O Curso de Pós-Graduação, Especialização à Distância - Tecnologias da Informação e da Comunicação Aplicadas à Educação (TIC) contempla os interesses de alunos egressos de cursos de licenciatura e que atuam no sistema de Ensino Fundamental e Médio e, também, de profissionais com interesse na Área de Tecnologias Aplicadas à Educação, incentivando a modalidade de Educação à Distância para o Sistema Universidade Aberta do Brasil – UAB.
</p>
 <p class="paragraph"> Neste sentido, valoriza os princípios de trabalhos construídos em sua experiência, avaliando as possibilidades de superação de fronteiras, implementando ações de qualificação, tendo presente as demandas da sociedade, ofertando políticas para a interiorização do ensino de pós-graduação gratuito e de qualidade. 

</p>
           <p class="paragraph"> O Curso de TIC não existe em versão presencial, sendo que foi  ofertado pela primeira vez, na UFSM, em 2007, e desde então tem sido ofertado à comunidade acadêmica, em parceria com a UAB.


</p>   
 <p class="paragraph">OBJETIVOS GERAL: Promover a formação continuada dos profissionais que atuam no Ensino Fundamental e Médio, assim como profissionais que possuam interesse na Área de Informática na Educação, para que possam não só utilizar de maneira adequada os recursos das TIC já ofertados, mas para que também atuem como gestores de produtos e de soluções empregando as TIC.



</p>               <br>
                    
